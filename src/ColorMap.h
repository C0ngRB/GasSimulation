#pragma once
#include <QtGui/QColor>
#include <algorithm>

/**
 * @file ColorMap.h
 * @brief 科学可视化颜色映射（热力图）
 *
 * 【设计目的】
 * 将归一化的气体浓度值映射为可见颜色，用于在Qt界面中可视化浓度分布。
 * 采用经典的热力图色谱（蓝→青→黄→红），直观表示低浓度到高浓度的过渡。
 *
 * 【颜色方案】
 * 采用四阶段分段线性插值（LERP），每个阶段的颜色选择基于：
 * - 低浓度（蓝色）：冷色调，表示安全/低风险区域
 * - 中低浓度（青色）：冷暖过渡起始
 * - 中高浓度（黄色）：警告色，表示需要注意
 * - 高浓度（红色）：暖色调，表示高浓度/危险区域
 *
 * 【色谱映射表】
 * | 归一化值 | 颜色名称 | RGB值     | 含义           |
 * |----------|----------|-----------|----------------|
 * | 0.00     | 深蓝     | (0, 0, 140) | 最低检测浓度   |
 * | 0.33     | 青色     | (0, 255, 255) | 中低浓度       |
 * | 0.66     | 黄色     | (255, 255, 0) | 中高浓度       |
 * | 1.00     | 红色     | (255, 0, 0)  | 最高浓度       |
 *
 * 【物理意义】
 * 在气体扩散模拟中，颜色直观反映浓度风险等级：
 * - 蓝色/青色区域：浓度低于检测阈值或安全水平
 * - 黄色区域：浓度达到需要注意的水平
 * - 红色区域：高浓度区域，可能需要预警
 *
 * 【使用场景】
 * MainWindow::renderTerrainAndSlice() 中使用此函数：
 * 1. 提取指定高度层的二维浓度切片
 * 2. 找到切片中的最大浓度值（用于归一化）
 * 3. 对每个网格点调用 colorMap(c/max) 获取颜色
 * 4. 将颜色绘制到QImage进行显示
 *
 * 【算法说明】
 * 使用分段线性插值而非纯RGB三线性插值，保证：
 * - 颜色过渡平滑自然
 * - 计算量小，适合实时渲染
 * - 颜色变化单调递增，符合直觉
 *
 * 【与Qt的集成】
 * 返回 QColor 对象，可直接用于 QImage::setPixelColor() 或 QPainter::setPen()。
 *
 * @param x 归一化浓度值，范围 [0, 1]
 *        - 0.0 表示浓度为0或低于检测下限
 *        - 1.0 表示当前切片中的最大浓度
 * @return QColor 对应的RGB颜色
 */
inline QColor colorMap(float x)
{
  // 钳制输入到有效范围，避免异常颜色
  x = std::clamp(x, 0.0f, 1.0f);

  // 线性插值辅助函数：在[a, b]区间内按比例t插值
  // t=0返回a，t=1返回b
  auto lerp = [](float a, float b, float t)
  { return a + (b - a) * t; };

  float r = 0, g = 0, b = 0;

  if (x < 0.33f)
  {
    // 第一阶段：深蓝(0,0,140) → 青色(0,255,255)
    // 适用于低浓度区域，冷色调表示安全
    float t = x / 0.33f;
    r = 0;
    g = lerp(0, 255, t);
    b = lerp(140, 255, t);
  }
  else if (x < 0.66f)
  {
    // 第二阶段：青色(0,255,255) → 黄色(255,255,0)
    // 从冷色向暖色过渡，视觉上提醒浓度增加
    float t = (x - 0.33f) / 0.33f;
    r = lerp(0, 255, t);
    g = 255;
    b = lerp(255, 0, t);
  }
  else
  {
    // 第三阶段：黄色(255,255,0) → 红色(255,0,0)
    // 高浓度区域，红色警示
    float t = (x - 0.66f) / 0.34f;
    r = 255;
    g = lerp(255, 0, t);
    b = 0;
  }

  return QColor((int)r, (int)g, (int)b);
}
